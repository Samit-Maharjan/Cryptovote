package com.example.cryptovote;

public class candidate {
    private String name;
    private String ID;

    public candidate(){

    }

    public candidate(String name, String ID) {
        this.name = name;
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }
}
